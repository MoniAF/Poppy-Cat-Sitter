    public void mostrarMensaje(String mensaje, String mensaje2){
        JOptionPane.showMessageDialog(null, mensaje +""+ mensaje2);
    }
    
    public void mostrarSplash(){
        panelSplashscreen.setVisible(true);
        panelMenu.setVisible(false);
        panelRegistro.setVisible(false);
        jMenuBar1.setVisible(false);
    }
    
    public void mostrarMenu(){
        panelSplashscreen.setVisible(false);
        panelMenu.setVisible(true);
        panelRegistro.setVisible(false);
        jMenuBar1.setVisible(true);
    }
    
    public void mostrarRegistro(){
        panelSplashscreen.setVisible(false);
        panelMenu.setVisible(false);
        panelRegistro.setVisible(true);
        jMenuBar1.setVisible(false);
    }
    
