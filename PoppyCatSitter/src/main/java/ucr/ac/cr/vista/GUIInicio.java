    public GUIInicio() {
        initComponents();
        controlador = new ControladorInicio(this, panelMenu, panelSplashscreen, panelRegistro);
        panelSplashscreen.escuchar(controlador);
        panelMenu.escuchar(controlador);
        panelMenu.ocultarCancelar();
        panelMenu.mostrarRegistro();
        panelRegistro.escuchar(controlador);
        panelRegistro.ocultarCancelado();
        this.escuchar(controlador);
        controlador.reproducirMusica();
        mostrarSplash();
        mostrarCreditos(false);
        mostrarVersion(false);
        mostrarComentarios(false);
        botonCerrarMB(false);
        this.instru1(false);
        this.instru2(false);
        this.instru3(false);
        this.botonCerrar(false);
        this.setExtendedState(MAXIMIZED_BOTH);
    }

